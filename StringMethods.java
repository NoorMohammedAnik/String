
package String;
public class StringMethods {
    
    public static void main(String[] args) {
        
        String icecream="I like Icecream";
        String person ="I am a person "; 
        
       
        System.out.println("String length= "+icecream.length());
        System.out.println("To Lower case= "+icecream.toLowerCase());
        System.out.println("To Upper case= "+icecream.toUpperCase());
        System.out.println("Trim = "+icecream.trim());
        System.out.println("charAt = "+icecream.charAt(4));
        System.out.println("Concatenate = "+icecream+" " +person);
       
        
    }
    
}
